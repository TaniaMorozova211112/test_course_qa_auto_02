class Rhomb:

    def __init__(self, сторона_а, кут_а):
        self.сторона_а = сторона_а
        self.кут_а = кут_а  # кут_б обчислюється автоматично

    # Метод __setattr__ — це спеціальний (магічний) метод у Python,
    # який автоматично викликається кожного разу,
    # коли ми присвоюємо значення атрибуту об'єкта.
    def __setattr__(self, name, value):
        # Контролюємо значення атрибутів 'сторона_а' та 'кут_а'
        if name == 'сторона_а':
            if value <= 0:
                raise ValueError("Сторона повинна бути більшою за 0")
        elif name == 'кут_а':
            if not (0 < value < 180):
                raise ValueError("Кут повинен бути між 0 і 180 градусами")
            # Автоматично розраховуємо кут_б
            super().__setattr__('кут_б', 180 - value)
        # Забороняємо змінювати кут_б вручну
        elif name == 'кут_б':
            raise AttributeError(
                "Кут_б обчислюється автоматично, його не можна встановлювати напряму"
            )

        # Зберігаємо атрибут
        super().__setattr__(name, value)


# Перевірка
rhomb = Rhomb(сторона_а=105, кут_а=30)

print(f"Сторона a: {rhomb.сторона_а}")
print(f"Кут a: {rhomb.кут_а}")
print(f"Кут b: {rhomb.кут_б}")


